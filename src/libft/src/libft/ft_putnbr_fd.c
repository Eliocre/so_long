#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	int_str;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
		if (n <= 9)
		{
			int_str = n + 48;
			write(fd, &int_str, 1);
		}
	}
}
